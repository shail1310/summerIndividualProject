    private void populateTable(){
         DefaultTableModel dtm = (DefaultTableModel) patientTreatmentInformation.getModel();

        // If it is already having something.... Flush it
        int totalRows = dtm.getRowCount();
        for (int i = (totalRows - 1); i >= 0; i--) {
            dtm.removeRow(i);
        }

        // Insert Objects
        for (Diagnosis ds : this.dignosisHist.getDiagnosisList()) {
            Object row[] = new Object[4];

            row[0] = ds;
            row[1] = ds.getDiagnosisInfo();
            row[2] = ds.getDoctorName();
            row[3] = ds.getTreatmentDate();
            dtm.addRow(row);
        }
    }
    
