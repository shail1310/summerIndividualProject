    private void viewJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewJButtonActionPerformed
        int selectedRow = patientTreatmentInformation.getSelectedRow();

        if (selectedRow >= 0) {
            Diagnosis dignosis = (Diagnosis) patientTreatmentInformation.getValueAt(selectedRow, 0);
            txtTreatment.setText(dignosis.getDiagnosisInfo());
            txtProblem.setText(dignosis.getProblem());
            txtDate.setText(dignosis.getTreatmentDate());
            txtDoctorName.setText(dignosis.getDoctorName());

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_viewJButtonActionPerformed

