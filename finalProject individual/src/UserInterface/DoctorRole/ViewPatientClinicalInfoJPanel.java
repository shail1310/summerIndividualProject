    private void populateAllergyTable(){
        DefaultTableModel model = (DefaultTableModel) alleryTable.getModel();

        model.setRowCount(0);
        for (Allergy allergy: patient.getAllergyInformation().getAllergyList()) {
                Object[] row = new Object[2];
                row[0] = allergy.getAllergyType();
                row[1] = allergy.getDescription();
                
                model.addRow(row);
            }
    }
    
    private void populateMedicationTable(){
        DefaultTableModel model = (DefaultTableModel) medicationTable.getModel();

        model.setRowCount(0);
        for (String medication: patient.getPatientMedications()) {
                Object[] row = new Object[1];
                row[0] = medication;
                
                model.addRow(row);
            }
    }
    
    private void populateTestResultsTable(){
        DefaultTableModel model = (DefaultTableModel) testResultsTable.getModel();

        model.setRowCount(0);
        for (String labTestResult: patient.getLabTestHistory()) {
                Object[] row = new Object[1];
                row[0] = labTestResult;
                
                model.addRow(row);
            }
    }
