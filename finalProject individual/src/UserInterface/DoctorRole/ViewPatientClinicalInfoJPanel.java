    private void viewMedicationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewMedicationActionPerformed
        int row = medicationTable.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        
        String medication = (String)medicationTable.getValueAt(row, 0);
        
        String fileLocation = WebMedical.getServerFileLocation() 
                + "\\" + patient.getPatientId() + "\\" + "Medications" + "\\" + medication;
        
        BaseViewer view = new BaseViewer();
        view.viewPdfFile(fileLocation);
    }//GEN-LAST:event_viewMedicationActionPerformed

