    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int row = enterpriseJTable.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a specific row");
            return;
        }
        
        Enterprise enterprise = (Enterprise) enterpriseJTable.getValueAt(row, 0);
        
        webMedical.getNetwork(enterprise.getNetName()).getEnterpriseDirectory().deleteEnterprise(enterprise);
        
        JOptionPane.showMessageDialog(null, "Enterprise successfully deleted");
    }//GEN-LAST:event_deleteButtonActionPerformed

