    private void enterpriseTypeComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterpriseTypeComboActionPerformed

        if (enterpriseTypeCombo.getSelectedItem() != null) {
            Enterprise.EnterpriseType enterpriseType = (Enterprise.EnterpriseType) 
                    enterpriseTypeCombo.getSelectedItem();

            if (enterpriseType.equals(Enterprise.EnterpriseType.FDA)) {
                stateCombo.setEnabled(false);
            }else{
                stateCombo.setEnabled(true);
            }
        }
    }//GEN-LAST:event_enterpriseTypeComboActionPerformed

