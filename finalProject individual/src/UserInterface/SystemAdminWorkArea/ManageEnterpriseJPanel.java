    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        Network network = (Network) networkCombo.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeCombo.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input for network!");
            return;
        }

        String name = nameTxt.getText();

        if (ProjectUtil.isFeildEmpty(name)) {
            JOptionPane.showMessageDialog(null, "Enter valid enterprise name");
            return;
        }

        if (type.equals(Enterprise.EnterpriseType.FDA)) {
            if (!network.getEnterpriseDirectory().isFDAEnterpriseExists()) {
                Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(
                        name, type, null);
                enterprise.setNetName(network.getCountryName());
                JOptionPane.showMessageDialog(null, "FDA Enterprise created successfully");
            } else {
                JOptionPane.showMessageDialog(null, "FDA enterprise for the network : " + network.getCountryName()
                        + " alreay exists!");
            }
            return;
        }
        
        String state = (String)stateCombo.getSelectedItem();
        if (ProjectUtil.isFeildEmpty(state)) {
            JOptionPane.showMessageDialog(null, "Select a  valid state for the enterprise");
            return;
        }

        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type, state);
        enterprise.setNetName(network.getCountryName());

        JOptionPane.showMessageDialog(null, "Enterprise successfully created");
        
        populateTable();

    }//GEN-LAST:event_submitJButtonActionPerformed

