    private void populateStateCombo(String network){
        stateCombo.removeAllItems();
        switch (network) {
            case "INDIA":
                stateCombo.addItem("Maharashtra");
                stateCombo.addItem("Delhi");
                stateCombo.addItem("Tamil Nadu");
                break;
            case "USA":
                stateCombo.addItem("Massechusets");
                stateCombo.addItem("New Jersey");
                stateCombo.addItem("Illinois");
                break;
            
            case "CHINA":
                stateCombo.addItem("Shanghai");
                stateCombo.addItem("Fujian");
                stateCombo.addItem("Tibet");
                break;
        }
    }
    
