    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (Network network : webMedical.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] row = new Object[4];
                row[0] = enterprise;
                row[1] = enterprise.getEntType().getValue();
                row[2] = network.getCountryName();
                row[3] = enterprise.getNetState();

                model.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        networkCombo.removeAllItems();
        enterpriseTypeCombo.removeAllItems();

        for (Network network : webMedical.getNetworkList()) {
            networkCombo.addItem(network);
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            enterpriseTypeCombo.addItem(type);
        }

    }

