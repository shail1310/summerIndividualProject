    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        Enterprise enterpriseName = (Enterprise) enterpriseComboBox.getSelectedItem();
        String username = usernameTxt.getText();
        String password = String.valueOf(pwdPasswordField.getPassword());
        String name = nameTxt.getText();

        if (ProjectUtil.isFeildEmpty(username) || ProjectUtil.isFeildEmpty(password)
                || ProjectUtil.isFeildEmpty(name)) {
            JOptionPane.showMessageDialog(null, "Please enter a valid userName, password and name for"
                    + " the enterprise admin");
            return;
        }

        if (enterpriseName.isEnterpriseAdminExists()) {
            JOptionPane.showMessageDialog(null, "Enterprise admin account already exist, please enter a new one");
            return;
        } 

        if(!WebMedical.checkIfUsernameIsUnique(username)){
            JOptionPane.showMessageDialog(null, "Username is already existing");
            return;
        }
        
        Employee employee = enterpriseName.getEmployeeDirectory().createEmployee(name);

        if (enterpriseName.getEntType().equals(Enterprise.EnterpriseType.DrugCompany)) {
            enterpriseName.getUserAccountDirectory().createEmployeeUserAccount(username, password, employee, new MedsProductionRole());
        } else if (enterpriseName.getEntType().equals(Enterprise.EnterpriseType.FDA)) {
            enterpriseName.getUserAccountDirectory().createEmployeeUserAccount(username, password, employee, new DrugAdminRole());
        } else {
            enterpriseName.getUserAccountDirectory().createEmployeeUserAccount(
                    username, password, employee, new AdminRole());
        }
        
        JOptionPane.showMessageDialog(null, "Enterprise Admin account successfully created");
        populateTable();
    }//GEN-LAST:event_submitJButtonActionPerformed

