    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int row = enterpriseTbl.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row you want to remove");
            return;
        }

        Enterprise enterprise = (Enterprise)enterpriseTbl.getValueAt(row, 0);
        UserAccount userAccount = (UserAccount) enterpriseTbl.getValueAt(row, 3);

        enterprise.getUserAccountDirectory().deleteUserAccount(userAccount);
        JOptionPane.showMessageDialog(null, "User Account deleted");
    }//GEN-LAST:event_deleteBtnActionPerformed

