    private void populateOrganizationComboBox(){
        organizationJComboBox.removeAllItems();
        
        for (Organization organization : organizationDir.getOrganizationList()){
            if(!organization.getOrganizationType().equals(Organization.Type.Admin)
                    && !organization.getOrganizationType().equals(Organization.Type.Patient)){
                organizationJComboBox.addItem(organization);
            }
        }
    }
    
    private void populateOrganizationEmpComboBox(){
        organizationEmpJComboBox.removeAllItems();
        
        for (Organization organization : organizationDir.getOrganizationList()){
            if(!organization.getOrganizationType().equals(Organization.Type.Admin)
                    && !organization.getOrganizationType().equals(Organization.Type.Patient)){
                organizationEmpJComboBox.addItem(organization);
            }
        }
    }

    private void populateTable(Organization organization){
        DefaultTableModel model = (DefaultTableModel) employeeJTable.getModel();
        
        model.setRowCount(0);
        
        for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()){
            Object[] row = new Object[2];
            row[0] = employee.getUserID();
            row[1] = employee;
            model.addRow(row);
        }
    }
