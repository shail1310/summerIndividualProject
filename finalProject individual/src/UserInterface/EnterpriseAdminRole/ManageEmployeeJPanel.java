    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int row = employeeJTable.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }

        Employee employee = (Employee) employeeJTable.getValueAt(row, 1);
        Organization org = (Organization)organizationJComboBox.getSelectedItem();

        org.getEmployeeDirectory().deleteEmployee(employee);
        JOptionPane.showMessageDialog(null, "Employee deleted");
    }//GEN-LAST:event_deleteButtonActionPerformed

